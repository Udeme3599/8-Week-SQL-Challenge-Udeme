-- upgrades from basic to monthly or pro plans are reduced by the current paid amount in that month and start immediately
with upgrade as (select customer_id, plan_id, start_date
from subscriptions
where customer_id in (
    select customer_id
    from subscriptions
    where plan_id in (1, 2, 3)
    group by customer_id
    having (
        count(distinct plan_id) = 3 -- Customers with plans 1, 2, and 3
        or 
        (count(distinct plan_id) = 2 and min(plan_id) = 1) -- Customers upgraded from plan 1 to 2 or 3
    )
)
and plan_id in (1, 2, 3))
update payments p
join upgrade u
on p.plan_id = u.plan_id
and p.plan_id = u.plan_id
set p.amount =
case 
when p.plan_id = 1 then 9.90  -- basic_montly price
when p.plan_id = 2 then (19.90 - 9.90) -- pro_monthly upgrade
when p.plan_id = 3 then (199.00 - 9.90) -- pro_annual upgrade
end;

-- ## updating the monthly payments to occur on the same day of month as the original start_date of any monthly paid plan

with upgrade as (
    select customer_id, plan_id, payment_date
    from payments
    where customer_id IN (
        select customer_id
        from payments
        where plan_id IN (1, 2)
        group by customer_id
        having count(distinct plan_id) = 2  -- Customers upgraded from plan 1 to plan 2
        and min(plan_id) = 1                -- Customers upgraded from plan 1 to plan 2
    )
    and plan_id in (1, 2)
),
date as (
    select 
        p2.customer_id, 
        p1.payment_date as plan_1_payment_date, 
        p2.payment_date as plan_2_payment_date,
        -- Generate the updated date for plan 2
        concat(
            year(p2.payment_date), '-',
            month(p2.payment_date), '-',
            -- If the day of plan 1 exceeds the valid days in plan 2's month, adjust it
            case 
                when day(p1.payment_date) > day(last_day(p2.payment_date)) 
                then day(last_day(p2.payment_date))  -- Adjust to last valid day of the month
                else day(p1.payment_date) 
            end
        ) as updated_plan_2_payment_date
    from payments p2
    join payments p1 
        on p2.customer_id = p1.customer_id
        and p2.plan_id = 2
        and p1.plan_id = 1
)
update payments p
join date d 
    on p.customer_id = d.customer_id
    and p.plan_id = 2
set p.payment_date = str_to_date(d.updated_plan_2_payment_date, '%Y-%m-%d')
where str_to_date(d.updated_plan_2_payment_date, '%Y-%m-%d') is not null;  -- Avoid invalid dates

