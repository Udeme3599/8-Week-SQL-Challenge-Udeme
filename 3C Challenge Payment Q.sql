# this query update the payment_order column in the payments table with a continuous count of how many times a customer has made a payment (based on their payment_date).
UPDATE payments p
JOIN (SELECT customer_id,payment_date, ROW_NUMBER() OVER (PARTITION BY customer_id ORDER BY payment_date) AS payment_order
    FROM payments) AS subquery
ON p.customer_id = subquery.customer_id
   AND p.payment_date = subquery.payment_date
SET p.payment_order = subquery.payment_order;

